    <h2>Movaldet</h2>
    <p>
      Movaldet - også kalt Mohølen -ligger på vestsiden av Gaula på på grensen mellom Melhus og
      Kvål i Melhus kommune, vel en mils vei oppstrøms munningen av elven. Valdet er ca 700 meter langt.
    </p>

    <p>
        Valdet fisker stort sett ved alle vannføringer - fra omkring 300 m<sup>3</sup>/s og helt ned 10-20 m<sup>3</sup>/s.
      Dette sikrer at man kan fiske selv om forholdene ikke nødvendigvis fremstår som de aller beste. Det er en
      kjensgjerning at Gaula kan være lunefull når det gjelder vannføring, og særlig mot slutten av sesongen kan man
      oppleve rimelige ”tørre” tilstander. Da er det godt å ha tilgang til en strekning som tåler variasjon i vannføring.
    </p>
    <p>
      Valdet er skjermet fra all trafikk og støy, og gir en sjelden villmarksfølelse. Vi tilbyr overnatting I egen
      hytte bare noen meter fra elvebredden.Movaldet leies på ukesbasis i 2012 sesongen
    </p>
